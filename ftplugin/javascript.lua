local tabsize = 2
vim.bo.tabstop = tabsize -- size of a hard tabstop (ts).
vim.bo.shiftwidth = tabsize -- size of an indentation (sw).
vim.bo.expandtab = true -- always uses spaces instead of tab characters (et).
vim.bo.softtabstop = tabsize -- number of spaces a <Tab> counts for. When 0, feature is off (sts).
